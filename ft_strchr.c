#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char		d;
	size_t		i;

	i = 0;
	d = (char)c;
	while (s[i] != '\0')
	{
		if (s[i] == d)
			return ((char *)&s[i]);
		i++;
	}
	return (NULL);
}
